// ===== ランク基準ステータス =====
// モンスターランク (1-10) から基準ステータスを算出する。全100迷宮のバランスの
// 単一の調整点。個体差は mul (hp/atk などの倍率) で付ける。
// ・hp はプレイヤーの攻撃力カーブ (魂×5部位+武器) に対し通常2-3撃で倒せる量
// ・atk はプレイヤーの VIT 半減則 (combat.js) を踏まえた貫通量
export function monStats(rank, boss = false) {
  const r = Math.max(1, Math.min(10, rank));
  const s = {
    hp: Math.round(30 * Math.pow(r, 1.8)),
    atk: Math.round(12 + 3.2 * r * r),
    def: Math.round(2 * Math.pow(r, 1.5)),
    spd: 4 + Math.round(r * 0.9),
    soul: Math.round(10 * Math.pow(1.65, r - 1)),
    gold: Math.round(8 * Math.pow(1.7, r - 1)),
  };
  if (boss) {
    s.hp = Math.round(s.hp * 4.0);
    s.atk = Math.round(s.atk * 1.4);
    s.def = Math.round(s.def * 1.3);
    s.spd += 3;
    s.soul *= 8;
    s.gold *= 10;
  }
  return s;
}

// ===== モンスター定義ヘルパー =====
// すべてのモンスターはここを通る。スキーマ:
// { id, key, name, race, rank, desc, maxhp, hp, atk, def, spd, soul, gold,
//   art, palette, boss?, dropNormal?, dropRare?, soulClass? }
// soul は撃破時に回収できる Soul 量 (経験値の役割を兼ねる)。
// 注: def/spd は定義用の名前のまま。出現時に六大ステへ写像される
//     (combat.js makeEnemy: atk→ATK, def→VIT, spd→AGI)。
// dropNormal/dropRare は省略可 (省略時は game.js がランクから決定的に割り当てる)。
// soulClass を持つ人型は撃破時にまれに魂を落とす。
export function defMonster(def) {
  if (!def.id) throw new Error("monster id required");
  if (!ARTS[def.artKey || ""] && !def.art) throw new Error("unknown artKey: " + def.artKey + " (" + def.id + ")");
  const base = ARTS[def.artKey] || {};
  const m = {
    id: def.id, key: def.id,
    name: def.name || "???",
    race: def.race || "beast",
    element: def.element || "none",
    rank: def.rank || 1,
    desc: def.desc || "",
    maxhp: def.hp || 10, hp: def.hp || 10,
    atk: def.atk || 1, def: def.def || 0, spd: def.spd || 5,
    soul: def.soul || 5, gold: def.gold || 3,
    art: def.art || base.art,
    palette: def.palette || base.palette,
    boss: !!def.boss,
  };
  if (def.dropNormal) m.dropNormal = def.dropNormal;
  if (def.dropRare) m.dropRare = def.dropRare;
  if (def.soulClass) m.soulClass = def.soulClass;
  if (def.elite) m.elite = true; // 強敵 (強敵階専用。通常プール・ミミックの対象外)
  // 役割持ち: role (healer/guard/summoner) + escort (取り巻きのid)。
  // summoner は summonKey の仲間を戦闘中に呼び寄せる。ability は明示指定で上書き可
  // (省略時は game.js が種族から決定的に付与する)
  if (def.role) m.role = def.role;
  if (def.escort) m.escort = def.escort;
  if (def.summonKey) m.summonKey = def.summonKey;
  if (def.ability !== undefined) m.ability = def.ability;
  // 個体ごとの戦闘特性 (図鑑にも掲載される):
  //   physResist : 物理被ダメを割合カット (0〜0.9)。「物理がほとんど効かない」表現
  //   magWeak    : 攻撃呪文の被ダメ倍率 (>1)。「魔法に弱い」表現
  //   regen      : 毎ラウンド最大HPの割合だけ自己回復 (0〜1)
  //   swift      : 出現時に AGI を底上げ (先手を取りやすい)
  //   evasive    : 物理攻撃を確率で大きく回避する
  //   pack       : 群れで現れる (出現数の下限を引き上げる)
  if (def.physResist) m.physResist = def.physResist;
  if (def.magWeak) m.magWeak = def.magWeak;
  if (def.regen) m.regen = def.regen;
  if (def.swift) m.swift = true;
  if (def.evasive) m.evasive = true;
  if (def.pack) m.pack = true;
  // 追加の戦闘特性 (combat.js が解釈):
  //   magResist   : 攻撃呪文の被ダメを割合カット (0〜0.9)。「魔法がほとんど効かない」
  //   enrage      : HPが3割を切ると一度だけ ATK/AGI が跳ね上がる
  //   endure      : 致死の一撃を一度だけ HP1 で耐える
  //   lifesteal   : 与えた物理ダメージの割合だけ自己回復 (0〜1)
  //   multistrike : 1手番で続けざまに攻撃する回数 (2〜4)
  //   barrier     : 被ダメを半減できる残り回数 (数回制)
  //   (ability に "warcry"=鼓舞 / "weaken"=弱体 も指定可)
  if (def.magResist) m.magResist = def.magResist;
  if (def.enrage) m.enrage = true;
  if (def.endure) m.endure = true;
  if (def.lifesteal) m.lifesteal = def.lifesteal;
  if (def.multistrike) m.multistrike = def.multistrike;
  if (def.barrier) m.barrier = def.barrier;
  if (def.traits) m.traits = def.traits; // 表示専用の追加特徴キー
  return m;
}

