export function msqReportLines(n) {
  const act = ACTS[actOf(n) - 1];
  if (n % 10 === 0) return act.finale;
  if (n % 5 === 0) return msqInterludeLines(n); // 幕間 (D5,15,25,…)
  return [PRAISES[hash(n, 73) % PRAISES.length], "「報酬を取らせる。」"];
}

// 第n章の報告報酬。幕の締め (n%10==0) は赤い魂が厚い
export function msqReward(n) {
  const act = actOf(n);
  const r = { gold: 80 + n * 22, soulPts: 30 + n * 6 };
  r.redSoul = n % 10 === 0 ? 8 + act * 4 : 2;
  return r;
}

