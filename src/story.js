//
// 100迷宮 = 20層 × 5迷宮。物語は20章 (= 20層) で進む:
//   ・層の最初の迷宮 (n%5==1) を拝命するとき → その層の「オープニング」(書き下ろし)
//   ・層の最後の迷宮 (n%5==0、層ボス戦) を踏破報告するとき → その層の「エンディング」(書き下ろし)
//   ・層の途中 (それ以外) の勅命/報告は、短いテンプレートを決定的に選ぶ
// 語り手は老王と、その傍らに立つ宰相。
//
// ── 物語の骨子 ──
// 迷宮は「死者の魂を喰らう病巣」として浄化を命じられるが、層が進むにつれ、
// 回収された魂の行き先 (王都へ吸い上げる「管」)・歴代の魂繰りの末路・器(人業)の正体・
// 王の若さの代償が明らかになる。最後の迷宮「終焉の玄室」の主は、魂の檻を守り続けた
// 最初の魂繰りの成れの果て(竜)。竜を討てば檻は開き、魂は巡りに還り、王は老いる。
