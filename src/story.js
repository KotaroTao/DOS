export function msqOrderLines(n) {
  const act = ACTS[layerOf(n) - 1];
  const dn = DUNGEONS[n - 1];
  const isBoss = n % 5 === 0; // 層末はボス戦、それ以外は最深部踏破
  const target = isBoss ? `「${dn.name}」を踏破し、その主を討て。` : `「${dn.name}」を踏破せよ。`;
  if (n % 5 === 1) return [...act.opening, `── 勅命: ${target}`];
  const o = SHARED_ORDERS[hash(n, 67) % SHARED_ORDERS.length].replaceAll("{dun}", dn.name);
  return [o, `── 勅命: ${target}`];
}

