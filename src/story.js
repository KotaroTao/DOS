// 勅命の対象迷宮 (n=1..100) の幕 (1..10)
export function actOf(n) { return Math.min(10, Math.ceil(n / 10)); }

// 第n章の勅命テキスト (受領時に表示)。幕の最初は書き下ろし、途中はテンプレート
export function msqOrderLines(n) {
  const act = ACTS[actOf(n) - 1];
  const dn = DUNGEONS[n - 1];
  const target = `「${dn.name}」を踏破し、その主を討て。`;
  if ((n - 1) % 10 === 0) return [...act.intro, `── 勅命: ${target}`];
  const o = act.orders[hash(n, 67) % act.orders.length].replaceAll("{dun}", dn.name);
  return [o, `── 勅命: ${target}`];
}

