// 戦士 (ダークファンタジー) — 大剣を地に突き立て両手で構える重騎士
// 中心 col 11/12 の間。頭は小さめ、胴を大きく、剣は手→鍔→刃と地へ繋げる。
const W = 24, H = 24;
const g = Array.from({ length: H }, () => Array(W).fill("."));
const ok = (x, y) => x >= 0 && x < W && y >= 0 && y < H;
const set = (x, y, c) => { if (ok(x, y)) g[y][x] = c; };
const fill = (x0, y0, x1, y1, c) => { for (let y = y0; y <= y1; y++) for (let x = x0; x <= x1; x++) set(x, y, c); };
const hline = (x0, x1, y, c) => { for (let x = x0; x <= x1; x++) set(x, y, c); };
const vline = (x, y0, y1, c) => { for (let y = y0; y <= y1; y++) set(x, y, c); };
const sym = (dx, y, c) => { set(11 - dx, y, c); set(12 + dx, y, c); }; // 中心対称ペア

// ===== 兜 (閉 じ た 大兜・小さめ・後ろへ反る角) =====
// ドーム
fill(9, 1, 14, 1, "4");
fill(8, 2, 15, 2, "4");
fill(8, 3, 15, 5, "4");
// 中央リッジ(明色)
vline(11, 1, 5, "2"); vline(12, 1, 5, "4");
// 後ろへ反る小角 (金属、先端 8=rank3+で金)
set(8, 2, "4"); set(7, 1, "4"); set(6, 1, "8");
set(15, 2, "4"); set(16, 1, "4"); set(17, 1, "8");
// 眼スリット (光る 6) と中央スリット
fill(8, 6, 15, 6, "4");
set(9, 6, "6"); set(10, 6, "6"); set(13, 6, "6"); set(14, 6, "6");
vline(11, 6, 8, "0"); vline(12, 6, 8, "0"); // 中央通気スリット
// 面頬
fill(8, 7, 15, 8, "4");
set(9, 7, "0"); set(14, 7, "0"); // 通気孔
// 兜下端
fill(9, 9, 14, 9, "4"); set(10, 10, "4"); set(13, 10, "4");

// ===== 胴鎧 (主色1 の陣羽織 + 板金4) =====
fill(7, 10, 16, 16, "1");
// 喉当て
fill(10, 9, 13, 9, "2");
// 脇の板金
vline(7, 10, 16, "4"); vline(16, 10, 16, "4");
// 胸の段(明色トリム)
hline(9, 14, 11, "2");
// 胸の紋章 (rank3+で金) — 剣の上部に重ねない位置
sym(2, 12, "5"); sym(2, 13, "5"); set(9, 12, "5"); set(14, 12, "5");

// ===== 肩鎧 (コンパクトな肩当て + 棘) =====
fill(4, 9, 7, 11, "4"); set(4, 8, "8");          // 左肩 + 棘
fill(16, 9, 19, 11, "4"); set(19, 8, "8");       // 右肩 + 棘
// 上腕 (側面を下りる)
fill(4, 12, 6, 14, "4"); fill(17, 12, 19, 14, "4");

// ===== 大剣 (中央・腰で両手持ち→地へ) =====
// 柄頭(石)と握りが胸から覗く
set(11, 11, "4"); set(12, 11, "4");
set(11, 12, "5"); set(12, 12, "5"); // 柄頭の石
vline(11, 13, 14, "2"); vline(12, 13, 14, "0"); // 握り
// 両手(籠手)で握る — 腰の位置
fill(8, 15, 15, 16, "4");
vline(11, 15, 16, "2"); vline(12, 15, 16, "0");
// 鍔 (横一文字、翼端が下がる)
hline(7, 16, 17, "4"); set(6, 17, "4"); set(17, 17, "4");
set(7, 18, "4"); set(16, 18, "4");
// 刃 (rows18-23 下方・4幅 fuller7・先細り)
for (let y = 18; y <= 21; y++) { set(10, y, "4"); set(11, y, "7"); set(12, y, "7"); set(13, y, "4"); }
set(11, 22, "7"); set(12, 22, "7"); set(11, 23, "4"); set(12, 23, "4"); // 切先

// ===== 脚 (刃を挟んで開脚・グリーヴ) =====
fill(7, 17, 9, 22, "1"); vline(8, 18, 21, "4"); // 左脚 (脛当て)
fill(14, 17, 16, 22, "1"); vline(15, 18, 21, "4"); // 右脚
hline(7, 9, 23, "0"); hline(14, 16, 23, "0");      // 足甲

// ===== 外周だけ輪郭 (内側の穴は残す: 端からの flood fill) =====
const bg = Array.from({ length: H }, () => Array(W).fill(false));
const st = [[0, 0]];
bg[0][0] = g[0][0] === ".";
while (st.length) {
  const [x, y] = st.pop();
  for (const [dx, dy] of [[1, 0], [-1, 0], [0, 1], [0, -1]]) {
    const nx = x + dx, ny = y + dy;
    if (ok(nx, ny) && !bg[ny][nx] && g[ny][nx] === ".") { bg[ny][nx] = true; st.push([nx, ny]); }
  }
}
const solid = (x, y) => ok(x, y) && g[y][x] !== "." && g[y][x] !== "0";
for (let y = 0; y < H; y++) for (let x = 0; x < W; x++) {
  if (g[y][x] === "." && bg[y][x] &&
    (solid(x - 1, y) || solid(x + 1, y) || solid(x, y - 1) || solid(x, y + 1) ||
     solid(x - 1, y - 1) || solid(x + 1, y - 1) || solid(x - 1, y + 1) || solid(x + 1, y + 1))) {
    g[y][x] = "0";
  }
}

const art = g.map((r) => r.join(""));
console.log(JSON.stringify(art, null, 0)
  .replace(/","/g, "\",\n    \"")
  .replace(/^\["/, "[\n    \"")
  .replace(/"\]$/, "\",\n  ]"));
